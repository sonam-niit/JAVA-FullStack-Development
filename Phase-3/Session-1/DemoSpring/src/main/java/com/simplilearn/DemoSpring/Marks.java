package com.simplilearn.DemoSpring;

public class Marks {

	private float phy;
	private float chem;
	private float math;
	public float getPhy() {
		return phy;
	}
	public void setPhy(float phy) {
		this.phy = phy;
	}
	public float getChem() {
		return chem;
	}
	public void setChem(float chem) {
		this.chem = chem;
	}
	public float getMath() {
		return math;
	}
	public void setMath(float math) {
		this.math = math;
	}
	
	
}
